<template>
    <h1>Створення</h1>
    <form v-on:submit.prevent>
        <label>
            Запитання
            <input name="question" v-model="faq.question">
        </label>
        <br>
        <label>
            Відповідь
            <input name="answer" v-model="faq.answer">
        </label>
        <br>
        <label>
            URL відео
            <input name="video" v-model="faq.video" />
        </label>
        <br />
        <button @click="createFaq" class="btn">
            Створити
        </button>
    </form>
</template>

<script>
import network from '@/network';
export default {
    name: "CreateFaq",
    data() {
        return {
            faq: {
                question: "",
                answer: "",
                video:""
            }
        }
    },
    methods: {
        async createFaq() {
            this.faq = await network.makeRequest("faq", "POST", this.faq);
            alert("Додано");
            setTimeout(() => this.$router.push("/"), 1000);
        }
    }
};
</script>

